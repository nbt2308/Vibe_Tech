    /**
     * Run the database seeds.
     * Mỗi brand 10 sản phẩm thực tế với thông số kỹ thuật, giá, hình ảnh.
     */
    public function run(): void
    {
        $products = $this->getProductsData();

        foreach ($products as $productData) {
            // Check if product already exists by SKU
            if (Product::where('sku', $productData['sku'])->exists()) {
                $this->command->warn("Đã tồn tại: {$productData['name']} (SKU: {$productData['sku']}), bỏ qua.");
                continue;
            }

            // Download thumbnail
            $thumbnailPath = $this->downloadImage(
                $productData['image_url'],
                'product-thumbnail',
                Str::slug($productData['name'])
            );

            if (!$thumbnailPath) {
                // Nếu không tải được ảnh, tạo placeholder
                $this->command->warn("Không tải được ảnh cho: {$productData['name']}, tạo placeholder.");
                $thumbnailPath = $this->createPlaceholder('product-thumbnail', Str::slug($productData['name']), $productData['name']);
            }

            Product::create([
                'category_id'    => $productData['category_id'],
                'brand_id'       => $productData['brand_id'],
                'name'           => $productData['name'],
                'sku'            => $productData['sku'],
                'description'    => $productData['description'],
                'thumbnail'      => $thumbnailPath,
                'price'          => $productData['price'],
                'sale_price'     => $productData['sale_price'],
                'stock_quantity' => $productData['stock_quantity'],
                'slug'           => Str::slug($productData['name']),
                'status'         => 1,
                'attributes'     => $productData['attributes'],
            ]);

            $this->command->info("✓ Đã tạo: {$productData['name']}");
        }
    }

    /**
     * Download image from URL using cURL
     */
    private function downloadImage(string $url, string $folder, string $filename): ?string
    {
        try {
            $ch = curl_init();
            curl_setopt_array($ch, [
                CURLOPT_URL            => $url,
                CURLOPT_RETURNTRANSFER => true,
                CURLOPT_FOLLOWLOCATION => true,
                CURLOPT_MAXREDIRS      => 5,
                CURLOPT_TIMEOUT        => 30,
                CURLOPT_SSL_VERIFYPEER => false,
                CURLOPT_SSL_VERIFYHOST => false,
                CURLOPT_HTTPHEADER     => [
                    'User-Agent: Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
                    'Accept: image/avif,image/webp,image/apng,image/svg+xml,image/*,*/*;q=0.8',
                    'Accept-Language: en-US,en;q=0.9',
                    'Referer: https://www.google.com/',
                ],
            ]);

            $imageContent = curl_exec($ch);
            $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
            $contentType = curl_getinfo($ch, CURLINFO_CONTENT_TYPE);
            curl_close($ch);

            if ($httpCode !== 200 || empty($imageContent) || strlen($imageContent) < 1000) {
                return null;
            }

            // Detect extension from content type
            $extension = 'jpg';
            if (stripos($contentType, 'png') !== false) $extension = 'png';
            elseif (stripos($contentType, 'webp') !== false) $extension = 'webp';
            elseif (stripos($contentType, 'gif') !== false) $extension = 'gif';
            elseif (stripos($contentType, 'svg') !== false) $extension = 'svg';

            $path = "{$folder}/{$filename}-" . Str::random(8) . ".{$extension}";
            Storage::disk('public')->put($path, $imageContent);

            return $path;
        } catch (\Exception $e) {
            return null;
        }
    }

    /**
     * Create a simple placeholder image with product name
     */
    private function createPlaceholder(string $folder, string $filename, string $productName): string
    {
        // Create a simple colored image placeholder
        $colors = [
            [41, 128, 185],   // Blue
            [39, 174, 96],    // Green
            [142, 68, 173],   // Purple
            [211, 84, 0],     // Orange
            [44, 62, 80],     // Dark Blue
            [192, 57, 43],    // Red
            [22, 160, 133],   // Teal
        ];
        $color = $colors[array_rand($colors)];

        $img = imagecreatetruecolor(500, 500);
        $bgColor = imagecolorallocate($img, $color[0], $color[1], $color[2]);
        $textColor = imagecolorallocate($img, 255, 255, 255);
        imagefill($img, 0, 0, $bgColor);

        // Draw product name on center
        $text = mb_substr($productName, 0, 25);
        $fontSize = 5;
        $textWidth = imagefontwidth($fontSize) * strlen($text);
        $x = max(10, (500 - $textWidth) / 2);
        imagestring($img, $fontSize, (int) $x, 240, $text, $textColor);

        ob_start();
        imagepng($img);
        $imageContent = ob_get_clean();
        imagedestroy($img);

        $path = "{$folder}/{$filename}-" . Str::random(8) . ".png";
        Storage::disk('public')->put($path, $imageContent);

        return $path;
    }

